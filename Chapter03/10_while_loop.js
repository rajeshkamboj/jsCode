// Question: Write a program to print the multiplication table of a given number using while loop.

let n = 5;

let i=1;
while(i<=10){
    console.log(n + " * " + i + " = " + n*i);
    i++;
}

// Output:
// Enter a number: 5
// 5 * 1 = 5
// 5 * 2 = 10
// 5 * 3 = 15
// 5 * 4 = 20
// 5 * 5 = 25
// 5 * 6 = 30
// 5 * 7 = 35
// 5 * 8 = 40
// 5 * 9 = 45
// 5 * 10 = 50

